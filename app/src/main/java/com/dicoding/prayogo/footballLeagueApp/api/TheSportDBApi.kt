package com.dicoding.prayogo.footballLeagueApp.api

import android.net.Uri
import com.dicoding.prayogo.footballLeagueApp.BuildConfig

object TheSportDBApi {
    fun getTeams(team: String?): String {
        return Uri.parse(BuildConfig.BASE_URL).buildUpon()
            .appendPath("api")
            .appendPath("v1")
            .appendPath("json")
            .appendPath(BuildConfig.TSDB_API_KEY)
            .appendPath("searchteams.php")
            .appendQueryParameter("s", "Soccer")
            .appendQueryParameter("t", team)
            .build()
            .toString()
    }

    fun getLeague(country:String?): String {
        return Uri.parse(BuildConfig.BASE_URL).buildUpon()
            .appendPath("api")
            .appendPath("v1")
            .appendPath("json")
            .appendPath(BuildConfig.TSDB_API_KEY)
            .appendPath("search_all_leagues.php")
            .appendQueryParameter("s", "Soccer")
            .appendQueryParameter("c", country)
            .build()
            .toString()
    }

    fun getPreviousMatch(idLeague:String?): String {
